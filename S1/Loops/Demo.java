package S1.Loops;

public class Demo {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 7) {

            System.out.println(i);

            i++;
        }
        System.out.println("--------");
        int j = 1;
        do {
            System.out.println(j);

            j++;

        }

        while (j <= 7);

    }
}

