package S1.Loops;

public class whileLoop1_printNum_from_1_to_5 {
    public static void main(String[] args) {
        int i = 1;          //1;start num

//6<=5

        while (i <= 5)      //2;end num

        {

            System.out.println(i);     //statement

            i++;//6

        }
    }


}
