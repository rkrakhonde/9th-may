package S1.Loops;

public class Dowhile_printNumFrom_1_to_5
{
public static void main(String[]args) {

    int i = 1;
//1<=5

    while (i <= 5) {
        System.out.println(i);
        i++;
    }
    System.out.println("--------");
    int j = 1;
    do {
        System.out.println(j);

        j++;
    }

    while (j <= 5);
//11<5
}
}
